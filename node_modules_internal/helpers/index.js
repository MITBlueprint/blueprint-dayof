function Helpers() {}

/*
* Takes a file or directory path as input and returns the current directory
*/
Helpers.currentDirectory = function(path) {
	var pathComponents = path.split('/');
	if (pathComponents.length == 0) {
		return undefined;
	}

	var lastComp = pathComponents[pathComponents.length - 1];

	//Check if given path was a filepath or dirpath
	if (lastComp.indexOf('.') == -1) {
		//If its a dirpath then we return last path component
		return lastComp;
	} else {
		//If its a filepath then we return  second to last path component
		if (pathComponents.length >= 2) {
			return pathComponents[pathComponents.length - 2];
		} else {
			//Situation ex: 'index.js' Can't tell directory
			return undefined;
		}
	}
}

/*
* Takes a file or directory path as input and returns the current directory
*/
Helpers.currentFileName = function(path) {
	var pathComponents = path.split('/');
	if (pathComponents.length == 0) {
		return undefined;
	}

	var lastComp = pathComponents[pathComponents.length - 1];

	//Check if given path was a filepath or dirpath
	if (lastComp.indexOf('.') == -1) {
		//If its a dirpath then there's no file name
		return undefined;
	} else {
		return lastComp.split('.')[0];
	}
}

module.exports = Helpers;